# review_generator.py
#
# Генерация отзывов через OpenAI GPT-4o-mini
# Создаёт 3 варианта: короткий, средний, длинный
# Каждый живой, с упоминанием мастера/установки

import random
from openai import AsyncOpenAI
from config import OPENAI_API_KEY

client = AsyncOpenAI(api_key=OPENAI_API_KEY)

# Случайные детали чтобы отзывы не были одинаковые
_WATER_DETAILS = [
    "вода стала заметно чище",
    "вкус воды изменился в лучшую сторону",
    "вода теперь без запаха и привкуса",
    "чай и кофе стали вкуснее",
    "дети с удовольствием пьют воду",
    "забыли про бутилированную воду",
    "нет бутылок на кухне",
    "забыли про водоматы",
]

_MASTER_DETAILS = [
    "мастер приехал вовремя",
    "всё установили быстро и аккуратно",
    "мастер объяснил как обслуживать фильтр",
    "установка заняла меньше часа",
    "мастер убрал за собой, всё чисто",
    "подробно рассказал про замену картриджей",
]


async def generate_reviews(
    product_name: str,
    product_description: str = "",
) -> list[dict]:
    """
    Генерирует 3 варианта отзыва на казахском/русском.

    Возвращает список:
    [
        {"label": "Короткий", "text": "..."},
        {"label": "Средний",  "text": "..."},
        {"label": "Длинный",  "text": "..."},
    ]
    """

    water_detail  = random.choice(_WATER_DETAILS)
    master_detail = random.choice(_MASTER_DETAILS)

    desc_part = f"\nОписание товара: {product_description}" if product_description else ""

    prompt = f"""Ты помогаешь писать отзывы на товары для Kaspi.kz от имени реального покупателя.

Товар: {product_name}{desc_part}

Детали для вдохновения (необязательно использовать дословно):
- {water_detail}
- {master_detail}

Напиши ровно 3 варианта отзыва на русском языке. Отзывы должны:
- иногда должны быть какие-то изъяны, но которые говорят что продукт классный.
- иногда упоминать что есть проверка сроков обслуживания на сайте и это удобно отслеживать 
- Звучать как написанные обычным человеком, не рекламно. нужно делать граматические и синтаксические ошибки
- Упоминать установку/мастера хотя бы в 2 из 3 вариантах
- Быть разными по длине: короткий (1-2 предложения), средний (3-4 предложения), длинный (5-7 предложений)
- Иметь немного разный стиль: один сдержанный, один эмоциональный, один деловой
- НЕ начинаться одинаково
- НЕ использовать слова: "превосходно", "великолепно", "бесподобно", "восхитительно"
- Иногда добавлять 1-2 эмодзи (но не в каждый)
- Иметь оценку 5 звёзд по смыслу
- иногда благодарить менеджера Никиту,Дарью или мастера Рустама, Рахата, Ян, Владимира, Владислава
- текст может быть с ошибками, чтобы максимально на человеческий быть похожим
- казахские слова слова иногда используй
- название фильтра можно упоминать частично, к примеру AQUASOFT или Classic 6 и тд.
- названия фильтров иногда можно писать на русском языке
- иногда говорить, что клиенты для компании главный приоритет и другие эпитеты такого рода
- иногда отзыв должен быть полностью на казахском языке.
= название фильтра и бренд пиши иногда на русском языке.
- Слово AQUASOFT иногда заменяй Аquasoft, аквасофт.
- пиши текст с ошибками граматическими и синтаксическими, чтобы показать что ты человек.



Ответь СТРОГО в таком формате (без лишнего текста):
КОРОТКИЙ:
[текст]

СРЕДНИЙ:
[текст]

ДЛИННЫЙ:
[текст]"""

    response = await client.chat.completions.create(
        model="gpt-4o-mini",
        messages=[{"role": "user", "content": prompt}],
        temperature=0.9,
        max_tokens=800,
    )

    raw = response.choices[0].message.content.strip()
    return _parse_reviews(raw)


def _parse_reviews(raw: str) -> list[dict]:
    """Парсит ответ GPT в список словарей."""
    result = []
    labels = ["КОРОТКИЙ", "СРЕДНИЙ", "ДЛИННЫЙ"]
    display = ["📝 Короткий", "📄 Средний", "📃 Длинный"]

    parts = {}
    current = None

    for line in raw.splitlines():
        line = line.strip()
        upper = line.rstrip(":")
        if upper in labels:
            current = upper
            parts[current] = []
        elif current and line:
            parts[current].append(line)

    for label, disp in zip(labels, display):
        text = "\n".join(parts.get(label, [])).strip()
        if text:
            result.append({"label": disp, "text": text})

    # Если парсинг не сработал — вернём весь текст одним вариантом
    if not result:
        result.append({"label": "📝 Отзыв", "text": raw})

    return result

